void	fill3(int r, int map[4][4])
{
	int	i;
	int	j;

	j = 0;
	while (j < 4)
	{
		i = 0;
		while (i < 4)
		{
			if (map[i][j] == 3)
				break ;
			if (i == 3)
			{
				map[r][j] = 3;
			}
			i++;
		}
		j++;
	}
}

void	ft_if_no_3(int map[4][4])
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (map[i][j] == 3)
				break ;
			if (j == 3)
			{
				fill3(i, map);
			}
			j++;
		}
		i++;
	}
}
